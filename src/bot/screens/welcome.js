const { Markup } = require('telegraf');

const WELCOME_TEXT =
  '🎯 Готовы забыть о курении за несколько дней?\n\n' +
  'NeuroTech Quit — это не очередная программа отказа от никотина, а авторский протокол, который уже помог тысячам людей стать некурящими.\n\n' +
  'Без мучений. Без постоянной борьбы с собой. Без срывов и бесконечных попыток «начать с понедельника».\n\n' +
  'Метод работает глубже, чем обычное подавление желания. Он направлен на сам корень зависимости, помогая перестроить внутреннюю реакцию на никотин и запустить процесс освобождения изнутри.\n\n' +
  'Важно: если у человека нет настоящего желания бросить курить и избавиться от зависимости, вероятность результата резко снижается. NeuroTech Quit работает тогда, когда внутри уже есть решение выйти из этой зависимости.\n\n' +
  'Первый шаг — понять, готовы ли вы сейчас.\n\n' +
  'Пройдите короткую диагностику готовности, и система подберёт для вас оптимальный маршрут прохождения протокола.';

const welcomeKeyboard = Markup.inlineKeyboard([
  [Markup.button.callback('О технологии и методе', 'welcome:intro_video')],
  [Markup.button.callback('Отзывы', 'welcome:reviews')],
]);

async function showWelcome(ctx) {
  try {
    await ctx.editMessageText(WELCOME_TEXT, welcomeKeyboard);
  } catch {
    await ctx.reply(WELCOME_TEXT, welcomeKeyboard);
  }
}

module.exports = { showWelcome };
