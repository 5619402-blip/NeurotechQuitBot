const { Markup } = require('telegraf');

const WHAT_AWAITS_TEXT =
  'Что вас ждёт дальше\n\n' +
  'Основной протокол NeuroTech Quit состоит из трёх процедур.\n\n' +
  'Первая процедура — «Антитабак». Она запускает основной процесс отказа от никотина.\n\n' +
  'Вторая процедура — «Быстрый рычаг». Её рекомендуется пройти через 1–2 дня после первой процедуры.\n\n' +
  'Третья процедура — закрепляющая. Это повторное прохождение процедуры «Антитабак». ' +
  'Её рекомендуется пройти через 2–3 дня после процедуры «Быстрый рычаг».\n\n' +
  'Дополнительно в вашем доступе есть Альфа-процедура.\n\n' +
  'Она проходит на альфа-частотах и помогает вернуть мозг в более спокойное альфа-состояние, ' +
  'снизить внутреннее напряжение, стресс и тревожность.\n\n' +
  'Альфа-процедуру можно использовать отдельно, когда вам нужно стабилизировать состояние, ' +
  'успокоиться и вернуться в более ровное внутреннее состояние.\n\n' +
  'Важно: если между процедурами у вас возникает желание курить, это нормально. ' +
  'Постарайтесь воздержаться от никотина до завершения всего курса.\n\n' +
  'После прохождения основного протокола при необходимости вы сможете вернуться к дополнительным ' +
  'сессиям в разделе «Мой доступ» и повторить процедуры по правилам доступа.\n\n' +
  'От имени NeuroTech желаем вам удачи, устойчивости и независимости от никотина.';

const whatAwaitsKeyboard = Markup.inlineKeyboard([
  [Markup.button.callback('Продолжить к протоколу', 'what_awaits:continue')],
]);

async function showWhatAwaits(ctx) {
  try {
    await ctx.editMessageText(WHAT_AWAITS_TEXT, whatAwaitsKeyboard);
  } catch {
    await ctx.reply(WHAT_AWAITS_TEXT, whatAwaitsKeyboard);
  }
}

module.exports = { showWhatAwaits };
