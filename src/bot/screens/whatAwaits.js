const { Markup } = require('telegraf');

const WHAT_AWAITS_TEXT =
  'Что вас ждёт дальше\n\n' +
  'В NeuroTech Quit есть два варианта прохождения.\n\n' +
  'Пробный вариант — это одна процедура «Антитабак».\n' +
  'Она помогает познакомиться с методом и пройти первый важный шаг в работе с никотиновой зависимостью.\n\n' +
  'Полный протокол — это последовательная программа из нескольких процедур.\n\n' +
  'Сначала проходит процедура «Антитабак».\n' +
  'Через 24 часа открывается процедура «Быстрый рычаг».\n' +
  'Через 1–2 дня после неё проходит закрепляющая процедура «Антитабак».\n' +
  'После этого по протоколу могут открываться укороченные процедуры для дополнительного закрепления результата.\n\n' +
  'Такой формат нужен для того, чтобы не просто снять первое напряжение, а постепенно закрепить новое состояние и снизить риск возврата к старой привычке.\n\n' +
  'В полном протоколе также доступна Альфа-процедура. Её можно использовать как поддержку, если появляется тревога, внутреннее напряжение или желание вернуться к никотину.\n\n' +
  'Если вы начнёте с пробной процедуры, её результат будет учтён. При переходе на полный протокол вы продолжите с нужного следующего шага.';

const whatAwaitsKeyboard = Markup.inlineKeyboard([
  [Markup.button.callback('Продолжить к протоколу', 'what_awaits:continue')],
]);

async function showWhatAwaits(ctx) {
  try {
    await ctx.editMessageText(WHAT_AWAITS_TEXT, whatAwaitsKeyboard);
  } catch {
    await ctx.reply(WHAT_AWAITS_TEXT, whatAwaitsKeyboard);
  }
}

module.exports = { showWhatAwaits };
